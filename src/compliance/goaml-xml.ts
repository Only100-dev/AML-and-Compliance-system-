/**
 * Generate CTR (Currency Transaction Report) XML
 * Mandatory for cash transactions >= AED 55,000 (FDL 10/2025).
 */
export function generateCTRXml(filing: GoAMLFilingData): string {
  const ref = filing.referenceNumber || `CTR-${Date.now()}`;
  const isCBWT = isCBWTThreshold(filing.amountAED);
  const lines: string[] = [
    generateXmlHeader('CTR'),
    `<goAML xmlns="${GOAML_NAMESPACE}" version="${GOAML_VERSION}">`,
    `  <ReportHeader>`,
    `    <ReportCode>${REPORT_TYPE_CODES.CTR}</ReportCode>`,
    `    <ReferenceNumber>${escapeXml(ref)}</ReferenceNumber>`,
    `    <FilingDate>${new Date().toISOString()}</FilingDate>`,
    `    <RegulatoryReference>FDL 10/2025</RegulatoryReference>`,
    `    <Jurisdiction>${escapeXml(filing.jurisdiction || 'AE')}</Jurisdiction>`,
    `    <SubmissionType>INITIAL</SubmissionType>`,
    `  </ReportHeader>`,
    generateReportingEntitySection(filing),
    generateSubjectSection(filing),
    generateTransactionSection(filing),
    generateNarrativeSection(filing),
    '  <CurrencyTransaction>',
    `    <ThresholdAmount>${CTR_THRESHOLD_AED.toFixed(2)}</ThresholdAmount>`,
    `    <ThresholdCurrency>${UAE_CURRENCY}</ThresholdCurrency>`,
    `    <ExceedsThreshold>${(filing.amountAED >= CTR_THRESHOLD_AED).toString()}</ExceedsThreshold>`,
    isCBWT ? `    <CBWT>true</CBWT>` : '',
    isCBWT ? `    <CBWTThreshold>${CBWT_THRESHOLD_AED.toFixed(2)}</CBWTThreshold>` : '',
    '    <ReportingObligation>FDL 10/2025 Article 15; CBUAE CTR Filing Requirements</ReportingObligation>',
    '  </CurrencyTransaction>',
    '</goAML>',
  ];
  return lines.filter(Boolean).join('\n');
}

