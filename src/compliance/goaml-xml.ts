// ─── Master XML Generation ──────────────────────────────────────────────────

/**
 * Generate goAML XML for any report type.
 * Automatically selects the correct template based on reportType.
 * Auto-detects CTR when amount >= AED 55,000.
 */
export function generateGoAMLXML(filing: GoAMLFilingData): string {
  const effectiveType = detectReportType(filing);

  switch (effectiveType) {
    case 'STR':
      return generateSTRXml({ ...filing, reportType: 'STR' });
    case 'SAR':
      return generateSARXml({ ...filing, reportType: 'SAR' });
    case 'CTR':
      return generateCTRXml({ ...filing, reportType: 'CTR' });
    case 'IFT':
      return generateIFTXml({ ...filing, reportType: 'IFT' });
    case 'PNMR':
      return generatePNMRXml({ ...filing, reportType: 'PNMR' });
    default:
      throw new Error(`Unsupported report type: ${effectiveType}`);
  }
}

// ─── XML Validation ─────────────────────────────────────────────────────────

/**
 * Validate the structure of a generated goAML XML document.
 * Performs structural checks (not full XSD validation).
 */
export function validateGoAMLXML(xml: string): XMLValidationResult {
  const errors: XMLValidationError[] = [];
  const warnings: string[] = [];

  // Check XML declaration
  if (!xml.startsWith('<?xml')) {
    errors.push({
      path: '/xml-declaration',
      message: 'Missing XML declaration',
      severity: 'error',
    });
  }

  // Check goAML root element
  if (!xml.includes('<goAML')) {
    errors.push({
      path: '/goAML',
      message: 'Missing goAML root element',
      severity: 'error',
    });
  }

  // Check namespace
  if (!xml.includes(GOAML_NAMESPACE) && !xml.includes('goaml')) {
    warnings.push('goAML namespace reference may be missing or incorrect');
  }

  // Check ReportHeader
  if (!xml.includes('<ReportHeader>')) {
    errors.push({
      path: '/goAML/ReportHeader',
      message: 'Missing ReportHeader section',
      severity: 'error',
    });
  } else {
    // Check required ReportHeader fields
    if (!xml.includes('<ReportCode>')) {
      errors.push({
        path: '/goAML/ReportHeader/ReportCode',
        message: 'Missing ReportCode in ReportHeader',
        severity: 'error',
      });
    }
    if (!xml.includes('<ReferenceNumber>')) {
      errors.push({
        path: '/goAML/ReportHeader/ReferenceNumber',
        message: 'Missing ReferenceNumber in ReportHeader',
        severity: 'error',
      });
    }
    if (!xml.includes('<FilingDate>')) {
      errors.push({
        path: '/goAML/ReportHeader/FilingDate',
        message: 'Missing FilingDate in ReportHeader',
        severity: 'error',
      });
    }
    if (!xml.includes('<Jurisdiction>')) {
      warnings.push('Jurisdiction field missing in ReportHeader (recommended for UAE filings)');
    }
  }

  // Check FDL 10/2025 reference
  if (!xml.includes('FDL 10/2025')) {
    warnings.push('FDL 10/2025 regulatory reference not found in XML header');
  }

  // Check ReportingEntity
  if (!xml.includes('<ReportingEntity>')) {
    errors.push({
      path: '/goAML/ReportingEntity',
      message: 'Missing ReportingEntity section',
      severity: 'error',
    });
  }

  // Check Subject
  if (!xml.includes('<Subject>')) {
    errors.push({
      path: '/goAML/Subject',
      message: 'Missing Subject section',
      severity: 'error',
    });
  }

  // Check Transaction (for CTR, STR, SAR, IFT)
  const reportCodeMatch = xml.match(/<ReportCode>(.*?)<\/ReportCode>/);
  const reportCode = reportCodeMatch?.[1] || '';
  if (['STR', 'SAR', 'CTR', 'IFT'].includes(reportCode) && !xml.includes('<Transaction>')) {
    errors.push({
      path: '/goAML/Transaction',
      message: 'Missing Transaction section for report type requiring transaction data',
      severity: 'error',
    });
  }

  // Check for amountAED (UAE-specific)
  if (xml.includes('<Transaction>') && !xml.includes('<AmountAED>')) {
    warnings.push('AmountAED field missing in Transaction (required for UAE CBUAE filings)');
  }

  // Check closing tag
  if (!xml.includes('</goAML>')) {
    errors.push({
      path: '/goAML',
      message: 'Missing goAML closing tag',
      severity: 'error',
    });
  }

  // Check for proper XML encoding
  if (!xml.includes('encoding="UTF-8"') && !xml.includes("encoding='UTF-8'")) {
    warnings.push('UTF-8 encoding declaration recommended for UAE FIU submissions');
  }

  // Check well-formedness: balanced tags
  const openTags = (xml.match(/<(\w+)[>\s]/g) || []).length;
  const closeTags = (xml.match(/<\/(\w+)>/g) || []).length;
  const selfClosingTags = (xml.match(/\/>/g) || []).length;
  if (Math.abs(openTags - closeTags - selfClosingTags) > 5) {
    warnings.push('XML tag balance appears off — potential malformation');
  }

  return {
    isValid: errors.filter((e) => e.severity === 'error').length === 0,
    errors,
    warnings,
  };
}

/**
 * Basic XSD structural validation.
 * Checks the XML against expected goAML v4.2 structure patterns.
 */
export function validateXSD(xml: string, xsdSchema?: string): XMLValidationResult {
  const result = validateGoAMLXML(xml);

  // If an XSD schema string is provided, perform additional structural checks
  if (xsdSchema) {
    // Check that the XML root element matches XSD target namespace
    const targetNsMatch = xsdSchema.match(/targetNamespace="([^"]+)"/);
    if (targetNsMatch) {
      const targetNs = targetNsMatch[1];
      if (!xml.includes(`xmlns="${targetNs}"`)) {
        result.errors.push({
          path: '/goAML',
          message: `XML namespace does not match XSD target namespace: ${targetNs}`,
          severity: 'error',
        });
      }
    }

    // Check version attribute from XSD
    const versionMatch = xsdSchema.match(/version="([^"]+)"/);
    if (versionMatch) {
      const xsdVersion = versionMatch[1];
      if (!xml.includes(`version="${xsdVersion}"`)) {
        result.warnings.push(`XML version attribute does not match XSD version: ${xsdVersion}`);
      }
    }

    // Check that required XSD elements are present in the XML
    const requiredElements = xsdSchema.match(/name="(\w+)"\s+type="[^"]*"\s+use="required"/g) || [];
    for (const elementMatch of requiredElements) {
      const nameMatch = elementMatch.match(/name="(\w+)"/);
      if (nameMatch) {
        const elemName = nameMatch[1];
        if (!xml.includes(`<${elemName}>`)) {
          result.errors.push({
            path: `/${elemName}`,
            message: `Required XSD element "${elemName}" not found in XML`,
            severity: 'error',
          });
        }
      }
    }
  }

  // Standard goAML v4.2 structural validation
  const structuralChecks = [
    { element: 'goAML', required: true },
    { element: 'ReportHeader', required: true },
    { element: 'ReportingEntity', required: true },
    { element: 'Subject', required: true },
    { element: 'Person', required: true },
    { element: 'FullName', required: true },
  ];

  for (const check of structuralChecks) {
    if (check.required && !xml.includes(`<${check.element}>`)) {
      result.errors.push({
        path: `/${check.element}`,
        message: `Required structural element "${check.element}" missing`,
        severity: 'error',
      });
    }
  }

  result.isValid = result.errors.filter((e) => e.severity === 'error').length === 0;
  return result;
}

