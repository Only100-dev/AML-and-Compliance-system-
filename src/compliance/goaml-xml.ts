/**
 * goAML XML Generation Utility — IC-OS Compliance Platform
 * Generates goAML v4.2 compliant XML for all 5 UAE FIU report types.
 * References: FDL 10/2025, CBUAE goAML Specification v4.2
 *
 * Report Types: STR, SAR, CTR, IFT, PNMR
 * UAE-Specific: amountAED, jurisdiction, emiratesId
 * Thresholds:  CTR auto-trigger at AED 55,000; CBWT at AED 3,500
 */

// ─── Types ──────────────────────────────────────────────────────────────────

export type GoAMLReportType = 'STR' | 'SAR' | 'CTR' | 'IFT' | 'PNMR';

export interface GoAMLFilingData {
  reportType: GoAMLReportType;
  referenceNumber?: string;
  subjectName: string;
  amountAED: number;
  currency?: string;
  narrative?: string;
  transactionDate?: string;
  jurisdiction?: string;
  emiratesId?: string;
  identifiers?: {
    passportNo?: string;
    nationality?: string;
    dateOfBirth?: string;
    phone?: string;
    email?: string;
    address?: string;
    accountNumber?: string;
    beneficiaryName?: string;
    beneficiaryAccount?: string;
    originatingCountry?: string;
    destinationCountry?: string;
    transactionType?: string;
  };
  filingEntity?: {
    name?: string;
    licenseNumber?: string;
    trn?: string;
    address?: string;
    contactPerson?: string;
    contactPhone?: string;
  };
}

export interface XMLValidationResult {
  isValid: boolean;
  errors: XMLValidationError[];
  warnings: string[];
}

export interface XMLValidationError {
  path: string;
  message: string;
  severity: 'error' | 'warning';
}

// ─── Constants ──────────────────────────────────────────────────────────────

const GOAML_VERSION = '4.2';
const GOAML_NAMESPACE = 'http://www.fatf-gafi.org/goaml/v4.2';
const CTR_THRESHOLD_AED = 55_000;
const CBWT_THRESHOLD_AED = 3_500;
const UAE_CURRENCY = 'AED';

const REPORT_TYPE_CODES: Record<GoAMLReportType, string> = {
  STR: 'STR',
  SAR: 'SAR',
  CTR: 'CTR',
  IFT: 'IFT',
  PNMR: 'PNMR',
};

// ─── XML Escape Helper ─────────────────────────────────────────────────────

function escapeXml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

// ─── XML Header Generator ──────────────────────────────────────────────────

function generateXmlHeader(reportType: GoAMLReportType): string {
  const timestamp = new Date().toISOString();
  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    `<!-- goAML v${GOAML_VERSION} | Report Type: ${reportType} | Generated by IC-OS Compliance Platform -->`,
    `<!-- Regulatory Reference: FDL 10/2025 (CBUAE Federal Decree-Law No. 10 of 2025 on Anti-Money Laundering) -->`,
    `<!-- Generated: ${timestamp} -->`,
  ].join('\n');
}

// ─── Reporting Entity Section ───────────────────────────────────────────────

function generateReportingEntitySection(filing: GoAMLFilingData): string {
  const entity = filing.filingEntity;
  return [
    '  <ReportingEntity>',
    `    <Name>${escapeXml(entity?.name || 'IC-OS Reporting Entity')}</Name>`,
    `    <LicenseNumber>${escapeXml(entity?.licenseNumber || 'CN-XXXX')}</LicenseNumber>`,
    entity?.trn ? `    <TRN>${escapeXml(entity.trn)}</TRN>` : '',
    entity?.address ? `    <Address>${escapeXml(entity.address)}</Address>` : '',
    entity?.contactPerson ? `    <ContactPerson>${escapeXml(entity.contactPerson)}</ContactPerson>` : '',
    entity?.contactPhone ? `    <ContactPhone>${escapeXml(entity.contactPhone)}</ContactPhone>` : '',
    `    <Jurisdiction>${escapeXml(filing.jurisdiction || 'AE')}</Jurisdiction>`,
    '  </ReportingEntity>',
  ].filter(Boolean).join('\n');
}

// ─── Subject / Person Section ───────────────────────────────────────────────

function generateSubjectSection(filing: GoAMLFilingData): string {
  const ids = filing.identifiers;
  return [
    '  <Subject>',
    '    <Person>',
    `      <FullName>${escapeXml(filing.subjectName)}</FullName>`,
    filing.emiratesId ? `      <EmiratesID>${escapeXml(filing.emiratesId)}</EmiratesID>` : '',
    ids?.passportNo ? `      <PassportNumber>${escapeXml(ids.passportNo)}</PassportNumber>` : '',
    ids?.nationality ? `      <Nationality>${escapeXml(ids.nationality)}</Nationality>` : '',
    ids?.dateOfBirth ? `      <DateOfBirth>${escapeXml(ids.dateOfBirth)}</DateOfBirth>` : '',
    ids?.phone ? `      <Phone>${escapeXml(ids.phone)}</Phone>` : '',
    ids?.email ? `      <Email>${escapeXml(ids.email)}</Email>` : '',
    ids?.address ? `      <Address>${escapeXml(ids.address)}</Address>` : '',
    '    </Person>',
    ids?.accountNumber ? `    <AccountNumber>${escapeXml(ids.accountNumber)}</AccountNumber>` : '',
    '  </Subject>',
  ].filter(Boolean).join('\n');
}

// ─── Transaction Section ────────────────────────────────────────────────────

function generateTransactionSection(filing: GoAMLFilingData): string {
  const ids = filing.identifiers;
  const txDate = filing.transactionDate || new Date().toISOString().split('T')[0];

  return [
    '  <Transaction>',
    `    <Date>${escapeXml(txDate)}</Date>`,
    `    <Amount>${filing.amountAED.toFixed(2)}</Amount>`,
    `    <Currency>${filing.currency || UAE_CURRENCY}</Currency>`,
    `    <AmountAED>${filing.amountAED.toFixed(2)}</AmountAED>`,
    ids?.transactionType ? `    <Type>${escapeXml(ids.transactionType)}</Type>` : '',
    ids?.originatingCountry ? `    <OriginatingCountry>${escapeXml(ids.originatingCountry)}</OriginatingCountry>` : '',
    ids?.destinationCountry ? `    <DestinationCountry>${escapeXml(ids.destinationCountry)}</DestinationCountry>` : '',
    '    <Beneficiary>',
    ids?.beneficiaryName ? `      <Name>${escapeXml(ids.beneficiaryName)}</Name>` : '',
    ids?.beneficiaryAccount ? `      <AccountNumber>${escapeXml(ids.beneficiaryAccount)}</AccountNumber>` : '',
    '    </Beneficiary>',
    '  </Transaction>',
  ].filter(Boolean).join('\n');
}

// ─── Narrative Section ──────────────────────────────────────────────────────

function generateNarrativeSection(filing: GoAMLFilingData): string {
  if (!filing.narrative) return '';
  return [
    '  <Narrative>',
    `    <Text>${escapeXml(filing.narrative)}</Text>`,
    '  </Narrative>',
  ].join('\n');
}

// ─── CTR Auto-Detection ─────────────────────────────────────────────────────

/**
 * Auto-detect if a CTR should be generated based on amount threshold.
 * Per FDL 10/2025, CTR is mandatory for cash transactions >= AED 55,000.
 */
export function shouldAutoGenerateCTR(amountAED: number): boolean {
  return amountAED >= CTR_THRESHOLD_AED;
}

/**
 * Check if the CBWT (Cross-Border Wire Transfer) threshold applies.
 * Per FDL 10/2025, enhanced due diligence applies at >= AED 3,500.
 */
export function isCBWTThreshold(amountAED: number): boolean {
  return amountAED >= CBWT_THRESHOLD_AED;
}

/**
 * Get the effective report type, auto-detecting CTR when applicable.
 */
export function detectReportType(filing: GoAMLFilingData): GoAMLReportType {
  if (filing.reportType !== 'CTR' && shouldAutoGenerateCTR(filing.amountAED)) {
    return 'CTR';
  }
  return filing.reportType;
}

// ─── Report-Specific XML Generators ─────────────────────────────────────────

/**
 * Generate STR (Suspicious Transaction Report) XML
 * Used for transactions suspected to be related to ML/TF.
 */
export function generateSTRXml(filing: GoAMLFilingData): string {
  const ref = filing.referenceNumber || `STR-${Date.now()}`;
  const lines: string[] = [
    generateXmlHeader('STR'),
    `<goAML xmlns="${GOAML_NAMESPACE}" version="${GOAML_VERSION}">`,
    `  <ReportHeader>`,
    `    <ReportCode>${REPORT_TYPE_CODES.STR}</ReportCode>`,
    `    <ReferenceNumber>${escapeXml(ref)}</ReferenceNumber>`,
    `    <FilingDate>${new Date().toISOString()}</FilingDate>`,
    `    <RegulatoryReference>FDL 10/2025</RegulatoryReference>`,
    `    <Jurisdiction>${escapeXml(filing.jurisdiction || 'AE')}</Jurisdiction>`,
    `    <SubmissionType>INITIAL</SubmissionType>`,
    `  </ReportHeader>`,
    generateReportingEntitySection(filing),
    generateSubjectSection(filing),
    generateTransactionSection(filing),
    generateNarrativeSection(filing),
    '  <SuspiciousActivity>',
    '    <Reason>Transaction flagged as suspicious per AML/CFT monitoring rules</Reason>',
    '    <DateDetected>' + new Date().toISOString().split('T')[0] + '</DateDetected>',
    '    <ReportingObligation>FDL 10/2025 Article 14</ReportingObligation>',
    '  </SuspiciousActivity>',
    '</goAML>',
  ];
  return lines.join('\n');
}

